graph_builder = StateGraph(OrchestratorState)

# nodes:
graph_builder.add_node("orchestrator", orchestrator_node)
graph_builder.add_node("Umoja_Workflow", umoja_graph)
graph_builder.add_node("Amadeus_Workflow", active_travel_graph)
graph_builder.add_node("Conversation_Workflow", conversation_graph)
graph_builder.add_node("Update_Journey_Workflow", update_journey_graph)
graph_builder.add_node("synthesizer", partial(synthesizer_node, llm=llm))

# entrypoint
graph_builder.set_entry_point("orchestrator")


def route_orchestrator(state: OrchestratorState) -> str:
    return state.get("route") or "direct"


graph_builder.add_conditional_edges(
    "orchestrator",
    route_orchestrator,
    {
        "Umoja_Workflow": "Umoja_Workflow",
        "Amadeus_Workflow": "Amadeus_Workflow",
        "Conversation_Workflow": "Conversation_Workflow",
        "Update_Journey_Workflow": "Update_Journey_Workflow",
        "direct": END,
    },
)

# after any worker, return to synthesizer then end
for worker in ("Umoja_Workflow", "Amadeus_Workflow", "Conversation_Workflow", "Update_Journey_Workflow"):
    graph_builder.add_edge(worker, "synthesizer")

def _decide_synth_return(state: OrchestratorState) -> str:
    # If there's a queued route from the orchestrator, continue to orchestrator
    if state.get("route_queue"):
        return "orchestrator"
    return "end"

graph_builder.add_conditional_edges(
    "synthesizer",
    _decide_synth_return,
    {
        "orchestrator": "orchestrator",
        "end": END,
    },
)

graph = graph_builder.compile(name="Orchestrator_Workflow")
