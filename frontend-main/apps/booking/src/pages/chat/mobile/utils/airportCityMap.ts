/**
 * Format an IATA code as "City (CODE)" or just "CODE" if unknown.
 */
export function formatAirportCity(iataCode: string): string {
  if (!iataCode) return "";
  const code = iataCode.toUpperCase().trim();
  const city = AIRPORT_CITY_MAP[code];
  return city ? `${city} (${code})` : code;
}

/**
 * Format an array of IATA stops into a route string.
 * e.g. ["ADD", "MLE"] → "Addis Ababa (ADD) → Malé (MLE)"
 * e.g. ["JFK", "IST", "DXB"] → "New York JFK (JFK) → Istanbul (IST) → Dubai (DXB)"
 */
export function formatRoute(stops: string[]): string {
  if (!stops || stops.length === 0) return "";
  return stops.map(formatAirportCity).join("  →  ");
}

