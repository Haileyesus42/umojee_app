def orchestrator_node(state: OrchestratorState) -> OrchestratorState | Command[OrchestratorState]:
    messages = state.get("messages", [])

    # Check if we're resuming from a queue (multi-task continuation)
    route_queue = state.get("route_queue", [])
    if route_queue:
        # Pop next task from queue
        target = route_queue.pop(0)
        if route_queue:
            state["route_queue"] = route_queue
        else:
            state.pop("route_queue", None)

        # Continue with queued task without re-deciding
        state["route"] = cast(OrchestratorState["route"], target)
        state["worker_cursor"] = len(state.get("messages", []))
        state.setdefault("worker_results", [])

        # Add context hint for this task if available
        tasks = state.get("tasks_identified", [])
        task_idx = len(state.get("accumulated_results", [])) if state.get("accumulated_results") else 0
        if task_idx < len(tasks):
            task_hint = f"[TASK {task_idx + 1}/{len(tasks)}]: {tasks[task_idx]}"
            # Prepend task context as a system message
            task_msg = SystemMessage(content=task_hint)

            # Extract context early if not already present (performance optimization)
            if not state.get("task_context"):
                with timer("early_context_extraction"):
                    extracted = extract_context_early(messages)
                    if extracted:
                        state["task_context"] = extracted

            if state.get("task_context"):
                task_msg.content += f"\nContext from previous tasks: {json.dumps(state['task_context'])}"
            state["messages"] = messages + [task_msg]

        return Command(goto=target, update=state)

    # New decision needed
    decision = _orchestrator_decide(messages, thread_has_title=bool(state.get("title")))

    # Support either a single `target` or a sequence via `targets`.
    raw_targets = decision.get("targets") or decision.get("target")
    if isinstance(raw_targets, list):
        targets = [str(t) for t in raw_targets]
    elif isinstance(raw_targets, str) and "," in raw_targets:
        targets = [t.strip() for t in raw_targets.split(",") if t.strip()]
    else:
        targets = [raw_targets] if raw_targets is not None else ["direct"]

    target = cast(str, targets[0] if targets else "direct")
    rationale = decision.get("rationale", "")

    # store title once if provided and not yet set
    if "title" in decision and not state.get("title"):
        state["title"] = decision["title"]

    # Store identified tasks for multi-task scenarios
    if "tasks_identified" in decision:
        state["tasks_identified"] = decision["tasks_identified"]

    # Initialize accumulated results for multi-task
    if len(targets) > 1:
        state.setdefault("accumulated_results", [])
        state.setdefault("task_context", {})

    # if the model wants to ask a missing-info question, append it and still route
    if q := decision.get("missing_info_question"):
        state["messages"] = messages + [AIMessage(content=q)]

    # forward setting (handled after worker finishes; see orchestrator_return)
    if "forward_from_agent" in decision:
        state["forward_from_agent"] = decision["forward_from_agent"]

    # direct reply path
    if target == "direct":
        reply = decision.get("direct_reply") or "(no reply provided)"
        state["messages"] = messages + [AIMessage(content=reply)]
        return state  # end

    # set the route and jump to that worker node
    state["route"] = cast(OrchestratorState["route"], target)
    state["worker_cursor"] = len(state.get("messages", []))
    state.setdefault("worker_results", [])
    # if multiple targets provided, store the remainder as a queue
    if len(targets) > 1:
        state["route_queue"] = targets[1:]
        # Add task hint for first task
        tasks = state.get("tasks_identified", [])
        if tasks:
            task_hint = f"[TASK 1/{len(targets)}]: {tasks[0]}"
            state["messages"] = messages + [SystemMessage(content=task_hint)]

    return Command(goto=target, update=state)


