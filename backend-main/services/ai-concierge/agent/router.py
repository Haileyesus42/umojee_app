MAX_TOKENS = 4000  # ~16k chars, safer token limit for context


def _trim_messages(messages: List[BaseMessage], max_tokens: int = MAX_TOKENS) -> List[BaseMessage]:
    """Optimize message history using intelligent trimming"""
    if not messages:
        return messages

    # Use performance-optimized message trimming
    with timer("message_optimization"):
        optimized = optimize_messages(
            messages,
            max_tokens=max_tokens,
            keep_system=True,
            keep_recent_human=3,  # Keep last 3 user messages for context
        )

    return optimized


def _has_title(messages: List[BaseMessage]) -> bool:
    # heuristic: if any past AI message contains "title" in JSON, assume it’s set
    for m in messages:
        if isinstance(m, AIMessage) and '"title"' in str(getattr(m, "content", "")):
            return True
    return False


def _orchestrator_decide(messages: List[BaseMessage], thread_has_title: bool) -> dict:
    """Run the LLM once to get JSON routing with performance optimizations"""

    # Check cache first (simple caching for repeated similar requests)
    cache_key = cache_key_for_decision(messages)
    cached = get_cached_decision(cache_key)
    if cached and not thread_has_title:  # Don't use cache for first message (need title)
        return cached

    schema_directive = SCHEMA_HINT_BASE + (
        "\nTitle directive: This is the FIRST decision; you MUST include 'title'."
        if not thread_has_title else
        "\nTitle directive: This thread already has a title; DO NOT include 'title'."
    )

    sys = SystemMessage(content=ORCH_INSTRUCTIONS.format(
        members="Umoja_Workflow, Amadeus_Workflow, Conversation_Workflow, Update_Journey_Workflow",
        active_travel_provider=ACTIVE_TRAVEL_PROVIDER_LABEL,
    ))
    schema = SystemMessage(content=schema_directive)

    # Optimize message history before LLM call
    with timer("message_trimming"):
        trimmed = _trim_messages(messages)

    # Invoke LLM with timing
    with timer("orchestrator_llm_decision"):
        out = llm.invoke([sys, schema] + trimmed)

    text = getattr(out, "content", "").strip()

    # Parse response with timing
    with timer("json_parsing"):
        try:
            data = json.loads(text)
            if not isinstance(data, dict):
                raise ValueError("Model did not return an object")
        except Exception:
            # fallback: minimal direct reply
            return {
                "target": "direct",
                "rationale": "LLM returned non-JSON; responding directly.",
                "direct_reply": text or "Sorry, I couldn't parse the decision."
            }

    # Cache the decision (except for first message with title)
    if not thread_has_title:
        cache_decision(cache_key, data)

    return data


