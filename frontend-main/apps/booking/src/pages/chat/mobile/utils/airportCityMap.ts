/**
 * Build a logo.dev URL for a given airport domain.
 */
function logoDevUrl(domain: string): string {
  return `https://img.logo.dev/${domain}?token=${LOGO_DEV_TOKEN}&size=128&format=png`;
}

/**
 * Get the airport logo URL for a given IATA code via logo.dev.
 * Returns undefined if the code is unknown or has no domain mapped.
 */
export function getAirportLogo(iataCode: string): string | undefined {
  if (!iataCode) return undefined;
  const code = iataCode.toUpperCase().trim();
  const info = AIRPORT_LOGO_MAP[code];
  return info?.domain ? logoDevUrl(info.domain) : undefined;
}

/**
 * Get the airport authority/company name for a given IATA code.
 */
export function getAirportName(iataCode: string): string | undefined {
  if (!iataCode) return undefined;
  const code = iataCode.toUpperCase().trim();
  return AIRPORT_LOGO_MAP[code]?.name || undefined;
}

