# -----------------------------
# State
# -----------------------------
class OrchestratorState(TypedDict, total=False):
    messages: list[BaseMessage]
    # set once on the first turn where the model provides it
    title: Optional[str]
    # routing results from the orchestrator's JSON
    route: Optional[
        Literal["Umoja_Workflow", "Amadeus_Workflow", "Conversation_Workflow", "Update_Journey_Workflow", "direct"]
    ]
    # if present, we will forward the last worker message verbatim to the user
    forward_from_agent: Optional[
        Literal["Umoja_Workflow", "Amadeus_Workflow", "Conversation_Workflow", "Update_Journey_Workflow"]
    ]
    # synthesized worker outputs captured after each worker finishes
    worker_results: List[WorkerResult]
    # message index snapshot taken right before a worker starts
    worker_cursor: int
    # queue of remaining routes for multi-task execution
    route_queue: Optional[List[str]]
    # identified tasks for multi-task scenarios
    tasks_identified: Optional[List[str]]
    # shared context that flows between tasks (e.g., flight details → car/hotel)
    task_context: Optional[dict]
    # accumulated results from all completed tasks
    accumulated_results: Optional[List[WorkerResult]]


